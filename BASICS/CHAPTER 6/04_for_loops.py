#for loops are used for sequential traversal
#for travesing list,string,tuples etc.
#example
list=[1,2,3,4,5]
for num in list:
    print(num)
#for loop for tuples
tup=(1,2,3,4,5,6)
for i in tup:
    print(i)
#for loop with else (optional else)
char="ashish"
for i in char:
    print(i)
else:
    print("loop ended")
