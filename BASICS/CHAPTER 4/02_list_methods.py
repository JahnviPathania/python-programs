list=[2,3,4]
#append - adds one element at the end
list.append(5)
list.append(1)
print(list)
#sort - sort in aescending order
list.sort()
print(list)
#sort in descending order
list.sort(reverse=True)
print(list)
#reverse the list
list.reverse()
print(list)
#insert - to add at a specific position
list.insert(1,5)
print(list)
#remove-remove first occurence of the element
list.remove(5)
print(list)
#pop method - remove element at specific index
list.pop(2)
print(list)



